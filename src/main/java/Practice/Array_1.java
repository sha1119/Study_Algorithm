package Practice;

import java.util.Arrays;

public class Array_1 {
    public static void main(String[] args) {
        int[] iArr = {80, 70, 60, 95, 100};
        for (int i = 0; i < iArr.length; i++)
            System.out.println(iArr[i]);
        System.out.println(Arrays.toString(iArr));
    }
}
